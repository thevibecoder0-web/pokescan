// --- LOCAL OCR ENGINE ---
// Fixed missing function required by components/Scanner.tsx
export const extractNameLocally = async (canvas: HTMLCanvasElement): Promise<string | null> => {
  try {
    if (!worker) {
      worker = await createWorker('eng');
    }

    const { data: { text } } = await worker.recognize(canvas);
    
    // Normalize text: remove non-alphabetical characters and convert to lowercase
    const words = text.toLowerCase().replace(/[^a-z\s]/g, '').split(/\s+/);
    
    // Look for a match in our local species database
    for (const word of words) {
      if (word.length < 3) continue;
      const match = POKEMON_SPECIES.find(species => 
        species.toLowerCase() === word
      );
      if (match) return match;
    }
  } catch (error) {
    console.warn("Local OCR analysis encountered an issue:", error);
  }

  return null;
};
